int	ft_strlen(char *str)
{
	int	count;
	
	count = 0;
	while (str[count] != '\0')
		count++;
	return count;
}

int	ft_strcmp(char *s1, char *s2)
{
	while (*s1 == *s2 && (*s1 != '\0'))
	{
		s1++;
		s2++;
	}
	return (*s1 - *s2);
}

